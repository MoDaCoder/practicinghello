public class ForLoops {
    public static void main(String[] args) {
        for(int i = 0; i < 5; i++) {
            System.out.println("Hello Mo" +i);
        }

        for(int i = 5; i > 0; i--) {
            System.out.println("Hello Mo" + i);
        }
    }
}

// To create a for loop you have a for followed by a parantheses
// Then we declare a loop or a counter variable with a semicolon at the end of it
// Then we write a boolean expression that determines how many times this loop is going to get executed

// First Java executes the loop declared or the counter variable
// Next Java evaluates the boolean condition
// Lastly Java increments the loop or counter variable and the body of the loop gets executed

// The for loop continues executing until the condition is met
