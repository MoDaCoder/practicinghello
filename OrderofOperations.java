public class OrderofOperations {
    public static void main(String[] args) {
        // Order of Operations are based on priority
        Float x = (1F+1F) * 2F / 3F + 4F - 5F;
        System.out.println(x);
    }
}

// Priority order in math:
// 1. Paranthases ()
// 2. Multiplication *
